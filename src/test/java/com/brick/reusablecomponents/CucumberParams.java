package com.brick.reusablecomponents;

public class CucumberParams {
	private String firstName;
	private String lastName;
	private String address;
	private String city;
	private String telephone;
	private String petName;
	private String petBirthday;
	private String petType;
	private String visitDiscription;

	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getPetName() {
		return petName;
	}
	public void setPetName(String petName) {
		this.petName = petName;
	}
	public String getPetBirthday() {
		return petBirthday;
	}
	public void setPetBirthday(String petBirthday) {
		this.petBirthday = petBirthday;
	}
	public String getPetType() {
		return petType;
	}
	public void setPetType(String petType) {
		this.petType = petType;
	}
	public String getVisitDiscription() {
		return visitDiscription;
	}
	public void setVisitDiscription(String visitDiscription) {
		this.visitDiscription = visitDiscription;
	}

}
